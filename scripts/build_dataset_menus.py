"""Generate the project-dataset menus surfaced on the site.

Writes (idempotently, between <!-- PROJECT-DATASETS --> markers):
  - a collapsible "Project datasets" panel with click-to-load cards + a how-to-load
    explainer into docs/playground-r.html and docs/playground-py.html, and
  - a "Project datasets" thumbnail gallery into the "Pick a dataset" section of
    docs/assignments.html.

Thumbnails come from docs/playground-data/<name>-thumb.png (mirrored there by
data/projects/_sync_to_playground.py). Node/edge counts are read live from the
CSVs; edit DATA below to add a dataset (key + one-line description), then run:

    python scripts/build_dataset_menus.py
"""
from __future__ import annotations

import re
from pathlib import Path
import pandas as pd

ROOT = Path(__file__).resolve().parent.parent
PROJECTS = ROOT / "data" / "projects"
GH = "https://github.com/timothyfraser/netsci/tree/main/data/projects"

# key -> one-line description (counts are read from the CSVs)
DATA = [
    ("amazon-last-mile", "A week of package flow: hubs → stations → delivery zones."),
    ("uber-manhattan", "Bipartite driver↔rider ride-matching across downtown Manhattan."),
    ("semiconductor-supply", "Multi-tier global chip supply chain, raw materials → products."),
    ("aerospace-components", "An aircraft's bill-of-materials + supplier network."),
    ("mutualaid-quake", "Neighborhood mutual aid before / during / after an earthquake."),
    ("financial-contagion", "Interbank exposure network across a financial crisis."),
    ("airline-delays", "Route network with delay propagation over a day."),
    ("power-grid", "A regional electrical transmission grid."),
    ("campus-contact", "Campus face-to-face contact network during an outbreak."),
    ("opensource-deps", "An open-source package dependency graph."),
    ("trade-commodity", "International commodity trade across a supply shock."),
    ("reorg-comms", "Corporate comms before / during / after a reorg + layoff."),
    ("satellite-constellation", "Multi-operator LEO satellites, links & ground stations."),
    ("drone-components", "A drone's component + software dependency graph."),
    ("transit-multimodal", "A city's bus + metro network with neighborhood nodes."),
    ("satellite-supply-chain", "Multi-tier satellite supply chain: materials → subsystems → programs."),
    ("aircraft-supply-chain", "Multi-tier airplane supply chain: materials → systems → programs."),
    ("ups-ground-network", "Truck line-haul: plant→plant lanes (packages, trucks, distance, transit)."),
]

GRID = ("display:grid;grid-template-columns:repeat(auto-fill,minmax(248px,1fr));"
        "gap:10px;margin-top:12px;")
CARD = ("display:flex;gap:10px;align-items:center;padding:9px 11px;"
        "background:rgba(5,46,22,0.45);border:1px solid var(--border-soft);"
        "border-radius:10px;text-align:left;color:inherit;font:inherit;"
        "width:100%;cursor:pointer;text-decoration:none;")
IMG = "width:56px;height:56px;border-radius:8px;flex:none;"
NM = "font-family:var(--font-mono);font-weight:700;font-size:12px;color:var(--white);"
CT = "color:var(--green-mint);font-size:11px;"
DS = "color:var(--grey);font-size:11px;"


def _counts():
    out = {}
    for key, _ in DATA:
        n = len(pd.read_csv(PROJECTS / key / "nodes.csv"))
        e = len(pd.read_csv(PROJECTS / key / "edges.csv"))
        out[key] = (n, e)
    return out


def _inner(key, n, e, desc):
    return (f'<img src="playground-data/{key}-thumb.png" alt="" style="{IMG}">'
            f'<span><span style="{NM}">{key}</span><br>'
            f'<span style="{CT}">{n} nodes · {e:,} edges</span><br>'
            f'<span style="{DS}">{desc}</span></span>')


def _panel(lang, counts):
    readfn = 'read.csv("…-nodes.csv")' if lang == "r" else 'pd.read_csv("…-nodes.csv")'
    builder = ("igraph (<code>graph_from_data_frame()</code>)" if lang == "r"
               else "networkx (<code>nx.from_pandas_edgelist()</code>) or igraph")
    cards = "\n".join(
        f'        <button type="button" class="ds-card" style="{CARD}" '
        f'onclick="loadSample(\'{k}\')" title="Load {k} into this session">'
        f'{_inner(k, *counts[k], d)}</button>' for k, d in DATA)
    return f"""  <!-- PROJECT-DATASETS (generated by scripts/build_dataset_menus.py) -->
  <details>
    <summary>Project datasets — what's available &amp; how to load them</summary>
    <div class="disclosure-body">
      <p>These larger networks <strong>ship with the playground — you don't upload anything.</strong>
      Pick one from the <strong>▾ Load sample</strong> menu in the toolbar, or click a card below.
      That fetches the dataset's CSV files into your in-browser working directory and drops a
      starter snippet into the editor. From there they're ordinary files: read them with
      <code>{readfn}</code> and build the graph with {builder}. Each dataset has a full codebook
      on <a href="{GH}" target="_blank" rel="noopener">GitHub</a>.</p>
      <div style="{GRID}">
{cards}
      </div>
    </div>
  </details>
  <!-- /PROJECT-DATASETS -->
"""


def _gallery(counts):
    # Thumbnail grid only — the heading, blurb, and buttons live in the
    # hand-maintained "Pick a dataset" block of assignments.html.
    cards = "\n".join(
        f'        <a class="ds-card" style="{CARD}" href="{GH}/{k}" target="_blank" rel="noopener">'
        f'{_inner(k, *counts[k], d)}</a>' for k, d in DATA)
    return f"""      <!-- PROJECT-DATASETS (generated by scripts/build_dataset_menus.py) -->
      <div class="ds-gallery" style="{GRID}">
{cards}
      </div>
      <!-- /PROJECT-DATASETS -->
"""


def _insert(path, block, anchor):
    p = ROOT / path
    t = p.read_text()
    if "<!-- PROJECT-DATASETS" in t:
        t = re.sub(r"[ \t]*<!-- PROJECT-DATASETS.*?<!-- /PROJECT-DATASETS -->\n", block, t, flags=re.S)
    else:
        t = t.replace(anchor, block + anchor, 1)
    p.write_text(t)
    print(f"updated {path}")


def main():
    counts = _counts()
    _insert("docs/playground-r.html", _panel("r", counts), '  <div class="pg-grid">')
    _insert("docs/playground-py.html", _panel("py", counts), '  <div class="pg-grid">')
    _insert("docs/assignments.html", _gallery(counts),
            '    <div class="sec-label" style="margin-top: 28px;">Pick a case study')


if __name__ == "__main__":
    main()
